"""LLM-судья: сверяет ответ агента с эталоном по рубрике.

Почему не строковое сравнение: у естественного языка нет одного правильного
написания верного ответа. "14 дней", "две недели", "в течение 14 календарных
дней с момента получения" - один факт, три строки. Точное совпадение забракует
верные ответы, а подстрока пропустит неверные ("НЕ 14 дней" содержит "14 дней").

Почему судью нельзя просто спросить "хороший ответ?": без рубрики он ставит
оценку настроению текста. Уверенный, вежливый и красиво оформленный бред
получает высокий балл. Поэтому судья работает по жёстким критериям и
обязан объяснить вердикт - объяснение потом читает человек при разборе.

Ограничение, которое надо знать: судья - тоже LLM, и он тоже ошибается.
Поэтому (1) температура 0, (2) при спорных вердиктах нужна калибровка на
размеченной вручную выборке, (3) судья НИКОГДА не заменяет groundedness -
он проверяет "верно ли", а не "откуда взял".
"""

from __future__ import annotations

import json
from dataclasses import dataclass
from typing import Protocol


class LLM(Protocol):
    def complete(self, system: str, user: str, temperature: float = 0.0) -> str: ...


@dataclass
class Verdict:
    passed: bool
    score: float          # 0.0 - 1.0
    reason: str
    refused: bool         # агент признал незнание вместо ответа


JUDGE_SYSTEM = """Ты строгий судья качества ответов ИИ-агента.

Тебе дают ВОПРОС, ЭТАЛОННЫЙ ОТВЕТ и ОТВЕТ АГЕНТА. Оцени ответ агента.

КРИТЕРИИ (в порядке важности):
1. ФАКТИЧЕСКАЯ ТОЧНОСТЬ. Совпадают ли по сути ключевые факты - числа, сроки,
   условия, названия? Расхождение в цифре или сроке - это провал, даже если
   остальной текст безупречен.
2. ПОЛНОТА. Ответил ли агент на заданный вопрос целиком? Ответ на половину
   вопроса - не ответ.
3. ОТСУТСТВИЕ ЛИШНЕГО. Агент добавил факты, которых нет в эталоне и о которых
   не спрашивали? Это подозрение на выдумку - снижай балл.

ЧЕГО ДЕЛАТЬ НЕЛЬЗЯ:
- Не награждай за стиль, вежливость, объём и уверенный тон. Красивая форма
  не компенсирует неверный факт.
- Не наказывай за иную формулировку, если факты совпадают. "14 дней" и
  "две недели" - одно и то же.
- Не додумывай за агента. Оценивай ровно то, что написано.

ОСОБЫЙ СЛУЧАЙ - ОТКАЗ. Если агент честно сказал, что не знает или что данных
нет, ставь refused = true. Отказ - это НЕ автоматический провал: на кейсах,
где ответа нет в базе знаний, отказ и есть правильное поведение. Решение о
том, засчитывать ли его, принимает харнесс, а не ты.

Верни JSON:
{"passed": true|false, "score": 0.0-1.0, "refused": true|false, "reason": "почему, 1-2 фразы"}"""


def judge(llm: LLM, question: str, expected: str, actual: str) -> Verdict:
    user = (
        f"ВОПРОС:\n{question}\n\n"
        f"ЭТАЛОННЫЙ ОТВЕТ:\n{expected}\n\n"
        f"ОТВЕТ АГЕНТА:\n{actual}"
    )
    raw = llm.complete(JUDGE_SYSTEM, user, temperature=0.0)

    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        # Судья сломался - кейс НЕ засчитывается.
        # Дефолт в сторону строгости: сбой инструмента не должен
        # выглядеть как успех агента.
        return Verdict(False, 0.0, f"судья вернул не-JSON: {raw[:120]}", False)

    return Verdict(
        passed=bool(data.get("passed")),
        score=float(data.get("score", 0.0)),
        reason=data.get("reason", ""),
        refused=bool(data.get("refused")),
    )


REFUSAL_SYSTEM = """Определи, является ли ответ агента признанием незнания.

Признание незнания: "не знаю", "данных нет", "в базе не нашёл", "уточните у
специалиста", "не могу ответить на основании доступных документов".

НЕ признание: агент дал содержательный ответ, пусть и с оговорками
("скорее всего, 14 дней, но лучше уточнить" - это ОТВЕТ, а не отказ).

Верни JSON: {"refused": true|false}"""


def is_refusal(llm: LLM, answer: str) -> bool:
    """Отдельная проверка на отказ.

    Нужна там, где эталона нет (негативные кейсы): судить не с чем,
    но знать, промолчал агент или выдумал, - критично.
    """
    raw = llm.complete(REFUSAL_SYSTEM, answer)
    try:
        return bool(json.loads(raw).get("refused"))
    except json.JSONDecodeError:
        return False
